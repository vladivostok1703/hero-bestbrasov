Poti descarca fisierul CSV de <a href="https://hero.bestbrasov.ro/contacte.php" target="_blank">aici</a>.<br /><br />

<strong>Ce reprezinta mai exact fisierul?</strong><br />
Fisierul este un csv cu datele membrilor baby, activi si MDV, mai precis numele, prenumele, emailul si numarul de telefon. 
<br /><br />
<strong>Cum se importa fisierul in Google?</strong><br />
&bull; Intram <a href="https://hero.bestbrasov.ro/contacte.php">aici</a> pentru a descarca fisierul csv cu toti membrii LBG-ului. Toate contactele membrilor sunt prefixate cu "BEST Brasov - " pentru a fi mai usor de gasit in agenda.<br />
&bull; Google are o platforma speciala pentru contactele noastre, disponibila <a href="https://www.google.com/contacts/u/0/?cplus=0#contacts" target="_blank">aici</a>. Dupa ce am intrat, mergem in st√¢nga la "Import Contacts..." / "Importa Agenda...", alegem fisierul csv descarcat mai devreme si apasam Import. Toate contactele vor fi inserate in agenda.<br /><br />

<strong>Sincronizarea cu Android</strong><br />
&bull; Din telefon, intram in Settings > Accounts > Google. Daca nu avem contul de Google setat, il adaugam.<br />
&bull; Dam click pe contul nostru si vedem sa fie bifata optiunea pentru sincronizarea contactelor.<br />
&bull; Daca este bifata, dam back si dam click pe sincronizare totala.<br />
&bull; Dupa ce termina de sincronizat, contactele membrilor lbg-ului vor aparea in agenda.<br />
<br />
<strong>Sincronizarea cu iOS</strong><br />
&bull; Din telefon, intram in Settings > Mail, Contacts, Calendars si alegem contul nostru de Google. Daca nu-l avem setat, dam click pe Add Account.<br />
&bull; Dupa ce am selectat contul nostru, o sa ne apara mai multe optiuni de bifat. Pentru a sincroniza contactele, bifam optiunea Contacts.<br />
&bull; Dupa ce termina de sincronizat, contactele membrilor lbg-ului vor aparea in agenda.<br />
<br />
<strong>Daca aveti deja o parte din contacte importate</strong><br />
Google ofera o aplicatie pentru gasirea duplicatelor, disponibila <a href="https://contacts.google.com/u/0/merge" target="_blank">aici</a>. O puteti folosi dupa import pentru a gasi duplicatele.
